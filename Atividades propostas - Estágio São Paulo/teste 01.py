#primeira atividade 
#não é preciso informar que a variável é inteiro neste contexto do python, ele reconhece isso
indice = 13 #parêmtros indicados na atividade
soma = 0
k = 0

while k < indice: #enquanto k for menor que o índice, ou seja, menor que 13
     k = k +1; #k recebe um incremento
     soma = soma + k  #soama recebe o valor de soma mais o valor de k com o incremento
print(soma) #saída do valor total após o loop encerrado